package eu.datacrop.maize.model_repository.commons.enums;

/**********************************************************************************************************************
 * This enumeration contains classifications of Relationships between Assets.
 *
 * @author Angela-Maria Despotopoulou [Athens, Greece]
 * @since version 0.4.0
 *********************************************************************************************************************/
public enum RelationshipType {

    /******************************************************************************************************************
     * Indicates that the present Asset is installed on another Asset.
     *****************************************************************************************************************/
    IS_INSTALLED_ON("IS_INSTALLED_ON"),

    /******************************************************************************************************************
     * Indicates that the present Asset is located in another Asset.
     *****************************************************************************************************************/
    IS_LOCATED_IN("IS_LOCATED_IN"),

    /******************************************************************************************************************
     * Indicates that the present Asset is connected to another Asset.
     *****************************************************************************************************************/
    IS_CONNECTED_TO("IS_CONNECTED_TO"),

    /******************************************************************************************************************
     * Indicates that the present Asset is component of another Asset.
     *****************************************************************************************************************/
    IS_COMPONENT_OF("IS_COMPONENT_OF"),

    /******************************************************************************************************************
     * Indicates that the present Asset is implementation of another Asset.
     *****************************************************************************************************************/
    IS_IMPLEMENTATION_OF("IS_IMPLEMENTATION_OF"),

    /******************************************************************************************************************
     * Indicates that the present Asset is monitoring another Asset.
     *****************************************************************************************************************/
    IS_MONITORING("IS_MONITORING"),

    /******************************************************************************************************************
     * Indicates that the present Asset is testing another Asset.
     *****************************************************************************************************************/
    IS_TESTING("IS_TESTING"),

    /******************************************************************************************************************
     * Indicates that the present Asset is cooperating with another Asset.
     *****************************************************************************************************************/
    IS_COOPERATING_WITH("IS_COOPERATING_WITH"),

    /******************************************************************************************************************
     * Indicates that the present Asset is the oldest version of another Asset.
     *****************************************************************************************************************/
    IS_OLDEST_VERSION_OF("IS_OLDEST_VERSION_OF"),

    /******************************************************************************************************************
     * Indicates that the present Asset is the newest version of another Asset.
     *****************************************************************************************************************/
    IS_NEWEST_VERSION_OF("IS_NEWEST_VERSION_OF"),

    /******************************************************************************************************************
     * Indicates that the present Asset exchanges messages with another Asset.
     *****************************************************************************************************************/
    EXCHANGES_MESSAGES_WITH("EXCHANGES_MESSAGES_WITH"),

    /******************************************************************************************************************
     * Indicates that the present Asset depends on another Asset.
     *****************************************************************************************************************/
    DEPENDS_ON("DEPENDS_ON");

    /******************************************************************************************************************
     * The text representing the RelationshipType enumeration values.
     *****************************************************************************************************************/
    private final String text;

    /******************************************************************************************************************
     * Constructor of the RelationshipType enumeration.
     *
     * @param text The text representing the enumeration values.
     *****************************************************************************************************************/
    RelationshipType(String text) {
        this.text = text;
    }

    /******************************************************************************************************************
     * Transforms an RelationshipType enumeration object to String.
     *
     *  @return A string representation of the Object.
     *****************************************************************************************************************/
    @Override
    public String toString() {
        return text;
    }
}
