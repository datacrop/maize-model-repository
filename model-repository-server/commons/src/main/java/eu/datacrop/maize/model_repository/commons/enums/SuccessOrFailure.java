package eu.datacrop.maize.model_repository.commons.enums;

/**********************************************************************************************************************
 * This enumeration contains a result indicator to be returned from functions denoting Success or Failure.
 *
 * @author Angela-Maria Despotopoulou
 * @since version 0.3.0
 *********************************************************************************************************************/
public enum SuccessOrFailure {

    /******************************************************************************************************************
     * Indicates a successful result.
     *****************************************************************************************************************/
    SUCCESS("SUCCESS"),

    /******************************************************************************************************************
     * Indicates a non-successful result.
     *****************************************************************************************************************/
    FAILURE("FAILURE");

    /******************************************************************************************************************
     * The text representing the enumeration values.
     *****************************************************************************************************************/
    private final String text;

    /******************************************************************************************************************
     * Constructor of the ResponseCode enumeration.
     *****************************************************************************************************************/
    SuccessOrFailure(String text) {
        this.text = text;
    }

    /******************************************************************************************************************
     * Transforms a ResponseCode enumeration object to String.
     *
     * @return A string representation of the Object.
     *****************************************************************************************************************/
    @Override
    public String toString() {
        return text;
    }

}
